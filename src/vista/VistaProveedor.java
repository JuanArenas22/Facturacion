    public void SoloNumeros(java.awt.event.KeyEvent evt){
        char c = evt.getKeyChar();
        if(c<'0'||c>'9') evt.consume();
    }
    
    public void SoloLetras(java.awt.event.KeyEvent evt){
        char c = evt.getKeyChar();
        if((c<'a' || c>'z') && (c<'A' || c>'Z')) evt.consume();
        
    }

