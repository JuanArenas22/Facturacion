    private void BTNGUARDARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNGUARDARActionPerformed
        // TODO add your handling code here:

        String nombre = txtNombre_pro.getText();
        String telefono = txtTelefono.getText();
        String correo = txtCorreo.getText();

        Proveedor pro = new Proveedor(nombre, telefono, correo);

        if (!(nombre.equals("") || telefono.equals("") || correo.equals(""))) {

            if (controllerProveedor.guardarPro(pro)) {
                JOptionPane.showMessageDialog(null, "Se a guardado el proveedor");
                txtCodigo_Pro.setEnabled(false);
                limpiarCampos();
                listar();
            } else {
                JOptionPane.showMessageDialog(null, "No Se a guardado el proveedor");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debe llenar todos los datos");
        }


    }//GEN-LAST:event_BTNGUARDARActionPerformed

    private void BTNELIMINARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNELIMINARActionPerformed
        // TODO add your handling code here:
        try {
            int codigo = Integer.parseInt(txtCodigo_Pro.getText());

            if (controllerProveedor.eliminarProveedor(codigo)) {
                JOptionPane.showMessageDialog(null, "Se elimino el proveedor");
                txtCodigo_Pro.setEnabled(false);
                listar();
                limpiarCampos();

            } else {
                JOptionPane.showMessageDialog(null, "No se elimino el proveedor");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "DEBE INGRESAR EL CODIGO A ELIMINAR ", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_BTNELIMINARActionPerformed

    private void BTNBUSCARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNBUSCARActionPerformed
        // TODO add your handling code here:

        try {
            int codigo = Integer.parseInt(txtCodigo_Pro.getText());
            Proveedor pro = controllerProveedor.buscarProveedor(codigo);

            if (pro.getCodigo_proveedor() == codigo) {
                cargarInformacion(pro);
                txtCodigo_Pro.setEnabled(false);

            } else {
                JOptionPane.showMessageDialog(null, "No se encontro el proveedor");
                limpiarCampos();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "DEBE INGRESAR EL CODIGO A BUSCAR ", "Advertencia", JOptionPane.WARNING_MESSAGE);
            txtCodigo_Pro.setEnabled(true);
            txtCodigo_Pro.setBackground(Color.PINK);
        }


    }//GEN-LAST:event_BTNBUSCARActionPerformed

    private void BTNMODIFICARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNMODIFICARActionPerformed
        // TODO add your handling code here:

        try {
            int codigo = Integer.parseInt(txtCodigo_Pro.getText());
            String nombre = txtNombre_pro.getText();
            String telefono = txtTelefono.getText();
            String correo = txtCorreo.getText();

            Proveedor pro = new Proveedor(codigo, nombre, telefono, correo);

            if (controllerProveedor.modificarProveedor(pro)) {
                JOptionPane.showMessageDialog(null, "Se a modificar el proveedor");
                txtCodigo_Pro.setEnabled(false);
                listar();
                limpiarCampos();
            } else {
                JOptionPane.showMessageDialog(null, "No Se modifico el proveedor");

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Debe llenar todos los datos");
        }
        

    }//GEN-LAST:event_BTNMODIFICARActionPerformed

