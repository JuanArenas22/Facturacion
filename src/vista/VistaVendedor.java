    private void GUARDARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GUARDARActionPerformed
        // TODO add your handling code here:
        try {

            int cedula = Integer.parseInt(txtCedulaVendedor.getText());
            String nombre_vendedor = txtNombreVendedor.getText();
            String nombre_usuario = txtNombreUsuario.getText();
            String contrasena = txtContrasena.getText();

            Vendedor ven = new Vendedor(cedula, nombre_vendedor, nombre_usuario, contrasena);

            if (!(nombre_vendedor.isEmpty() || nombre_usuario.isEmpty() || contrasena.isEmpty())) {
                if (controlador.GuardarVendedor(ven)) {
                    JOptionPane.showMessageDialog(null, "Se Guardo el Vendedor");

                    limpiarCampos();
                    listar();
                } else {
                    JOptionPane.showMessageDialog(null, "Ya existe un vendedor con esa cedula");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Debe llenar los campos");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Debe llenar los campos", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_GUARDARActionPerformed

