    private void BUSCARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BUSCARActionPerformed

        try {
            int cedula = Integer.parseInt(txtCedulaVendedor.getText());
            Vendedor ven = controlador.buscarVendedor(cedula);

            if (ven.getCedula_vendedor() == cedula) {
                cargarcampos(ven);
            } else {
                JOptionPane.showMessageDialog(null, "No se encontro el vendedor");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Debe ingresar la cedula a buscar");
        }


    }//GEN-LAST:event_BUSCARActionPerformed

